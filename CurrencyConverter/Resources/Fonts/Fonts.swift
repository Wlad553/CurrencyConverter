import Foundation

enum Fonts {
    enum Lato {
        /// Light font weight which corresponds to the weight value of 300
        static let light = "Lato-Light"
        
        /// Regular font weight which corresponds to the weight value of 400
        static let regular = "Lato-Regular"
        
        /// Bold font weight which corresponds to the weight value of 700
        static let bold = "Lato-Bold"
        
        /// Black font weight which corresponds to the weight value of 900
        static let black = "Lato-Black"
    }
}
